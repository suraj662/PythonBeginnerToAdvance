# ==============================================================================
#                 OBJECT-ORIENTED PROGRAMMING (OOP) IN PYTHON
# ==============================================================================
# OOP is a programming paradigm (a way of organizing code). Instead of writing
# a loose list of instructions, you group related data (variables) and
# actions (functions) together into "Objects".
#
# Think of it like this:
# - CLASS: A blueprint for a car. It says cars have wheels, a color, and can drive.
# - OBJECT: Your actual red Honda Civic sitting in the driveway.
#
# The 4 Core Pillars of OOP are:
# 1. ENCAPSULATION: Hiding the internal state of an object.
# 2. INHERITANCE:   Creating new classes based on existing ones (Parent -> Child).
# 3. POLYMORPHISM:  Different objects responding to the same method in their own way.
# 4. ABSTRACTION:   Hiding complex reality while exposing only the necessary parts.
# ==============================================================================

